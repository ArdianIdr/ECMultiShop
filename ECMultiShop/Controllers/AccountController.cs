        [Authorize(Roles = "Admin")]
        public ActionResult Users()
        {
            var userId = User.Identity.GetUserId();

            var user = db.Users.Find(userId);

            if (user != null)
            {
                // Pass emrin e userit ne view
                ViewBag.UserName = user.UserName; 
            }
            else
            {
                ViewBag.UserName = "Unknown User";
            }

            ViewBag.CategoryId = new SelectList(db.Categories, "Id", "Name");
            ViewBag.SubCategoryId = new SelectList(db.SubCategories, "Id", "Name");
            var unreadNotifications = db.Notifications
         .Where(n => !n.IsRead)
         .OrderByDescending(n => n.CreatedAt)
         .Take(10) // vetem 10 notification (te fundit) - descending
         .ToList();

            // marrim stockin nga baza
            var lowStockProducts = db.Stocks
                .Where(s => s.Quantity <= 5 && s.IsActive) // ja bejm limitin 5 
                .Select(s => new LowStockProductViewModel
                {
                    ProductName = s.Product.Name,
                    StockQuantity = s.Quantity
                })
                .OrderBy(s => s.StockQuantity)
                .ToList();

            ViewBag.UnreadNotifications = unreadNotifications; // shfaqim notificationin
            ViewBag.LowStockProducts = lowStockProducts; 

            var users = db.Users.ToList();
            return View(users);
        }
        public ActionResult DeleteUser(string id)
        {
            // Sigurohem qe useri gjindet ne baze
            var user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound("User not found");
            }

            // Kontrolloj nese useri eshte admin (Admini ne baze eshte me id 1)
            var adminRoleId = "1"; // RoleId per Adminin = 1
            var isAdmin = db.Set<IdentityUserRole>().Any(ur => ur.UserId == id && ur.RoleId == adminRoleId);

            if (isAdmin)
            {
                // Tregoj qe useri eshte admin dhe nuk mund te fshihet nga baza
                TempData["ErrorMessage"] = "Cannot delete this user because they are an Admin.";
                return RedirectToAction("Users"); 
            }

            // Nese nuk eshte admin, fshije
            db.Users.Remove(user);
            db.SaveChanges();

            TempData["SuccessMessage"] = "User deleted successfully.";
            return RedirectToAction("Users"); 
        }

        public ActionResult MakeAdmin(string id)
        {
            var user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound("User not found");
            }

            // Kontrollojme nese useri eshte admin 
            var adminRoleId = "1"; // RoleId per Adminin = 1
            var userRole = db.Set<IdentityUserRole>().FirstOrDefault(ur => ur.UserId == id && ur.RoleId == adminRoleId);

            if (userRole != null)
            {
                return Content("User is already an Admin.");
            }

            // Add the admin role to the user
            db.Set<IdentityUserRole>().Add(new IdentityUserRole
            {
                UserId = id,
                RoleId = adminRoleId
            });

            db.SaveChanges();

            return RedirectToAction("Users"); // kthehemi ne liste
        }



